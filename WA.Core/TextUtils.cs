using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace WA.Core;

public static class TextUtils
{

    public static string GetExclamationMark()
    {
        return "\u0021";
    }

    public static string GetEuroSign()
    {
        return "\u20ac";
    }

    public static string GetThinkingFace()
    {
        return "\u1f914";
    }
}
